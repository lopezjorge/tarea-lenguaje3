    private void cambiarCliente() {
       
//        Cliente cliente = new Cliente(Integer.parseInt(txtCodigo.getText()), txtNOmbre.getText(), txtApellidos.getText(), txtDireccion.getText(), 
////                txtCelular.getText(), Double.parseDouble(txtCredito.getText()), btnEstado.isSelected());
           Cliente cliente = new Cliente();
           cliente.setCodigo(Integer.parseInt(txtCodigo.getText()));
           cliente.setNombres(txtNOmbre.getText());
           cliente.setApellidos(txtApellidos.getText());
           cliente.setDireccion(txtDireccion.getText());
           cliente.setCiudad_codigo(selectCiudad.getSelectedIndex()+1);
           cliente.setCelular(txtCelular.getText());
           cliente.setCredito(Double.parseDouble(txtCredito.getText()));
           cliente.setEstado(btnEstado.isSelected());
        
        boolean result = clienteController.modificarCliente(cliente);
        
            if(result){
             JOptionPane.showMessageDialog(this, "Cliente modificado con exito", "Aviso", 1);
         
        }
            limpiarCampos();
        
    }

