    private void consultarClientePorCodigo(int codigo) {
        
        btnGuardar.setEnabled(true);
        BtnCancelar.setEnabled(true);
        btnEliminar.setEnabled(true);
        btnEditar.setEnabled(true);
        
        if (codigo != 0) {
            
            Cliente cliente = clienteController.buscarClientePorCodigo(codigo);
            
            if (cliente != null) {
                txtNOmbre.setText(cliente.getNombres());
                txtApellidos.setText(cliente.getApellidos());
                txtDireccion.setText(cliente.getDireccion());
                //txtCiudad.setText(cliente.getCiudad());
                selectCiudad.setSelectedIndex(cliente.getCiudad_codigo()-1);
                txtCelular.setText(cliente.getCelular());
                txtCredito.setText(cliente.getCredito()+"");
                btnEstado.setSelected(cliente.isEstado());

            }else{
            JOptionPane.showMessageDialog(this, "No existe cliente con este codigo, reintente");
            txtCodigo.setText("");
            
                        
            
            }
        }else{
            JOptionPane.showMessageDialog(this, "Este campo solo admite numeros, reintente");
            txtCodigo.setText("");
        }
    }

